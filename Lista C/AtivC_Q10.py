# 10. Leia 2 números inteiros, calcule e escreva o quociente e o resto da divisão do 1o pelo 2o.

# Entrada 
numero_1 = int(input('Insira o número: '))
numero_2 = int(input('Insira o número: '))

# Processamento 
quociente = numero_1 // numero_2
resto = numero_1 % numero_2

# Saída
print(f'O quociente da divisão é {quociente} e o resto da divisão é {resto}.')