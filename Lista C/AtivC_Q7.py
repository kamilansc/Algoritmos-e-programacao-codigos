# 7. Leia 3 números, calcule e escreva a soma dos 2 primeiros e a diferença entre os 2 últimos.

# Entrada
numero_1 = int(input('Digite o primeiro número: '))
numero_2 = int(input('Digite o segundo número: '))
numero_3 = int(input('Digite o terceiro número: '))

# Processamento
soma = numero_1 + numero_2
diferença = numero_2 - numero_3

# Saída
print(f'A soma dos dois primeiros números é {soma} !!')
print(f'A diferença dos dois últimos números é {diferença} !!')