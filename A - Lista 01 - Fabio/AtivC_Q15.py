# 15. Leia o valor da base e altura de um triângulo, calcule e escreva sua área. (área=(base * altura)/2)

# Entrada
base = int(input('Digite o valor da base do triângulo: '))
altura = int(input('Digite o valor da altura do triângulo: '))

# Processamento
area = (base * altura) // 2

# Saída
print(f'A área do triângulo é igual a {area}')